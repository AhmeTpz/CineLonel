            </svg>
            <!-- AIRING TODAY ICON / BUGÜN YAYINLANAN İKONU -->
            <svg v-else-if="item.label === 'Airing Today'" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" class="w-4 h-4 text-green-600">
